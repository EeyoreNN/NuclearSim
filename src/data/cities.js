export function findNearestCity(lat, lng, radiusDeg = 5) {
  let nearest = null;
  let minDist = Infinity;
  for (const city of CITIES) {
    const dLat = city.lat - lat;
    const dLng = city.lng - lng;
    const dist = Math.sqrt(dLat * dLat + dLng * dLng);
    if (dist < minDist) { minDist = dist; nearest = city; }
  }
  return minDist <= radiusDeg ? nearest : null;
}

export function getStrategicTargets(nationCode) {
  return CITIES.filter(c => c.strategic >= 2 && (nationCode ? c.country === nationCode : true));
}
